package com.example.kotlin_proyect

class Operaciones {

    public var a:Int = 0
    public var b:Int = 0

    fun suma(): Int{
        return a+b
    }

    fun resta(): Int{
        return a-b
    }

    fun multiplicacion(): Int{
        return a*b
    }

    fun division(): Int{
        return a/b
    }

}